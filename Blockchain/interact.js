const contractAddress = '0xa10752dfcce22c50e1093ec85272dfa2515f44ef'; 

// Connect to the Ethereum network
const web3 = new Web3('https://sepolia.infura.io/v3/2c845505a1d74c80a4e8648f76a0d9a2'); // Replace with your Infura project ID or other provider

// Get the contract instance
const projectVoting = new web3.eth.Contract(contractABI, contractAddress);

const main = async () => {
  // Get the list of accounts
  const accounts = await web3.eth.getAccounts();
  const chairperson = accounts[0];

  console.log('Chairperson:', chairperson);

  // Register a voter
  try {
    await projectVoting.methods.registerVoter(accounts[1]).send({ from: chairperson });
    console.log(`Voter ${accounts[1]} registered successfully.`);
  } catch (error) {
    console.error('Error registering voter:', error);
  }

  // Cast a vote
  try {
    await projectVoting.methods.vote(0).send({ from: accounts[1] });
    console.log(`Voter ${accounts[1]} voted successfully.`);
  } catch (error) {
    console.error('Error casting vote:', error);
  }

  // Announce the winner
  try {
    const winner = await projectVoting.methods.winnerName().call();
    console.log('Winning proposal:', winner);
  } catch (error) {
    console.error('Error getting winner:', error);
  }
};

main();
